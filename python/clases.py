class calculadora():
	def suma(x,y):
		print('La suma es : ',x + y)

	def resta(x,y):
		print('La resta es : ',x - y)

	def multi(x,y):
		print('La multiplicación es : ',x * y)

	def div(x,y):
		print('La división es : ',x / y)

calculadora.suma(2,3)
calculadora.resta(2,3)
calculadora.multi(2,3)
calculadora.div(2,3)
